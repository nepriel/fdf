#include "libft.h"

int		ft_atoi(const char *str)
{
	int		sign;
	int		ret;

	sign = 1;
	ret = 0;
	while (*str == ' ' || (*str >= '\t' && *str <= '\r'))
		str++;
	if (*str == '-' || *str == '+')
	{
		sign = (*str == '-' ? -1 : 1);
		str++;
	}
	while (ft_isdigit(*str))
	{
		ret = (ret * 10) + (*str - '0');
		str++;
	}
	return (sign * ret);
}
